package Day01;

public class Ex08_Int {
	
	public static void main(String[] args) {
		int var1 = 10; //10진수
		System.out.println("var1 : " + var1);
		
		int var2 = 010; //8진수
		System.out.println("var2 : " + var2);
		
		int var3 = 0x10; //16진수
		System.out.println("var3 : " + var3);
		
	}

}
